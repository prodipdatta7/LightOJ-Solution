ull phi[N];
void pre() {
    for(int i = 1 ; i <= N ; i++) {
        phi[i] = i ;
    }
    for(int i = 2 ; i <= N ; i++) {
        if(phi[i] == i) {
            phi[i] = i-1 ;
            for(int j = i * 2 ; j <= N ; j+=i)
                phi[j] = (phi[j]/i) * (i-1) ;
        }
    }
    for(int i = 1 ; i <= N ; i++) {
        phi[i]*= phi[i];
        phi[i] += phi[i-1] ;

    }
}

